"""
Exercise  4: Assume that we execute the following assignment statements:

width = 17
height = 12.0

For each of the following expressions, write the value of the expression 
and the type (of the value of the expression).
"""

# 1. width//2
a = width//2
print(a)
print(type(a))

# 2. width/2.0
b = width/2.0
print(b)
print(type(b))

# 3. height/3
c = height/3
print(c)
print(type(c))

# 4. 1 + 2 * 5
d = 1 + 2 * 5
print(d)
print(type(d))
